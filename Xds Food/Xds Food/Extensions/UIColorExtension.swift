import UIKit

extension UIColor {
    
    static var backgroundGray: UIColor = {
        return UIColor(red: 40/255, green: 44/255, blue: 48/255, alpha: 1)
    }()
    
    static var customGreen: UIColor = {
        return UIColor(red: 122/255, green: 198/255, blue: 79/255, alpha: 1)
    }()
}
